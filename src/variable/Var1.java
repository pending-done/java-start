package variable;

public class Var1 {
    public static void main(String[] args) {
        // 1. 변수 선언 후 초기화
        int a;
        a = 1;
        System.out.println(a);

        //2. 변수 선언과 초기화 한번에
        int b = 2;
        System.out.println(b);

        // 3. 여러 변수 선언 및 초기화 한번에
        int c = 3, d = 4;
        System.out.println(c);
        System.out.println(d);




    }
}
